function yy=ga_cbab_RTN_fitness(c)
load tempdata.mat x11 x22 x33 yy eb eperp1 eperp2;
x1 = x11;
x2 = x22;
x3 = x33;
yt = yy;
% c(24) =
% [nc,nb,na,nab...
%  vcthp,vcthz,vbthp,vbthz,vathp,vathz,vathp,vathz...
%  vcswpara,vcswperp1,vcswperp2,...
%  vbswpara,vbswperp1,vbswperp2,...
