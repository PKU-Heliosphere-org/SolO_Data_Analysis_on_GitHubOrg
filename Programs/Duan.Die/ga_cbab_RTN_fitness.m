%  vabswpara,vabswperp1,vbaperp2]
yf = 1.e-3*c(1)/(c(5)^2*c(6)*pi^1.5).*exp(-((x1-c(13)).*eb(1)+(x2-c(14)).*eb(2)+(x3-c(15)).*eb(3)).^2/c(5)^2).*exp(-((x1-c(13)).*eperp1(1)+(x2-c(14)).*eperp1(2)+(x3-c(15)).*eperp1(3)).^2/c(6)^2).*exp(-((x1-c(13)).*eperp2(1)+(x2-c(14)).*eperp2(2)+(x3-c(15)).*eperp2(3)).^2/c(6)^2)+...
     1.e-3*c(2)/(c(7)^2*c(8)*pi^1.5).*exp(-((x1-c(16)).*eb(1)+(x2-c(17)).*eb(2)+(x3-c(18)).*eb(3)).^2/c(7)^2).*exp(-((x1-c(16)).*eperp1(1)+(x2-c(17)).*eperp1(2)+(x3-c(18)).*eperp1(3)).^2/c(8)^2).*exp(-((x1-c(16)).*eperp2(1)+(x2-c(17)).*eperp2(2)+(x3-c(18)).*eperp2(3)).^2/c(8)^2)+...
     1.e-3*c(3)/(c(9)^2*c(10)*pi^1.5).*exp(-((x1-c(19)).*eb(1)+(x2-c(20)).*eb(2)+(x3-c(21)).*eb(3)).^2/c(9)^2).*exp(-((x1-c(19)).*eperp1(1)+(x2-c(20)).*eperp1(2)+(x3-c(21)).*eperp1(3)).^2/c(10)^2).*exp(-((x1-c(19)).*eperp2(1)+(x2-c(20)).*eperp2(2)+(x3-c(21)).*eperp2(3)).^2/c(10)^2)+...
